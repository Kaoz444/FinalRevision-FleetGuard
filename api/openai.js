export default async function handler(req, res) {
    if (req.method !== 'POST') {
        return res.status(405).json({ error: 'M√©todo no permitido' });
    }

    const { prompt, image } = req.body;
    if (!prompt || !image) {
        return res.status(400).json({ error: 'Se requiere un prompt y una imagen v√°lida' });
    }

    // Estados y problemas predefinidos
    const predefinedConditions = {
        statuses: [
            "Condici√≥n √≥ptima",
            "Leve desgaste",
            "Desgaste moderado",
            "Requiere reparaci√≥n menor",
            "Requiere reparaci√≥n urgente",
            "Llanta ponchada",
            "No funcional"
        ],
        issues: [
            "No presenta problemas",
            "Sin desgaste visible",
            "Condici√≥n normal",
            "Presi√≥n baja visible",
            "Desgaste irregular",
            "Da√±o estructural visible",
            "P√©rdida total de presi√≥n",
            "Objeto punzante visible",
            "Deformaci√≥n visible",
            "Grietas visibles",
            "Desgaste excesivo"
        ]
    };

    try {
        // Validate API key
        if (!process.env.OPENAI_API_KEY) {
            throw new Error('OpenAI API key not configured');
        }

        const response = await fetch('https://api.openai.com/v1/chat/completions', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${process.env.OPENAI_API_KEY}`,
            },
            body: JSON.stringify({
                model: 'gpt-4o',  // Updated to the correct model name
                messages: [
                    {
                        role: 'system',
                        content: `Eres un experto en inspecci√≥n de veh√≠culos. Siempre responde en formato JSON siguiendo esta estructura:
                        {
                            "component": "Nombre del componente analizado",
                            "status": "Uno de: ${predefinedConditions.statuses.join(', ')}",
                            "issues": ["Lista de problemas detectados de: ${predefinedConditions.issues.join(', ')}"]
                        }
                        
                        üìå **Reglas Importantes:**
                        - Si la llanta est√° visiblemente desinflada, usa "Llanta ponchada".
                        - Si hay deformaciones visibles, usa "Requiere reparaci√≥n urgente".
                        - Si no hay da√±os, usa "Condici√≥n √≥ptima".
                        - Si hay duda entre dos estados, elige el m√°s severo.
                        - NO inventes informaci√≥n.
                        - Si la imagen es irreconocible, responde con:
                          {
                            "component": "Desconocido",
                            "status": "No determinado",
                            "issues": []
                          }`
                    },
                    {
                        role: 'user',
                        content: [
                            {
                                type: 'text',
                                text: `Analiza esta llanta utilizando exclusivamente estos estados y problemas:`
                            },
                            {
                                type: 'image_url',
                                image_url: {
                                    url: `data:image/jpeg;base64,${image}`
                                }
                            }
                        ]
                    }
                ],
                max_tokens: 150
            })
        });

        const data = await response.json();

        // Validate OpenAI response
        if (!response.ok) {
            console.error('‚ùå Error en la respuesta de OpenAI:', JSON.stringify(data, null, 2));
            return res.status(response.status).json({ error: data.error?.message || 'Error en OpenAI' });
        }

        if (!data.choices?.[0]?.message?.content) {
            console.error('‚ùå Respuesta inv√°lida de OpenAI:', JSON.stringify(data, null, 2));
            return res.status(500).json({ error: 'Formato incorrecto en la respuesta de OpenAI' });
        }

        // Parse and validate JSON response
        let parsedResponse;
        try {
            const content = data.choices[0].message.content.trim();
            // Remove markdown code block if present and parse JSON
            const jsonContent = content.replace(/```json\s*|\s*```/g, '').trim();
            
            parsedResponse = JSON.parse(jsonContent);
            
            console.log("‚úÖ Respuesta JSON recibida de OpenAI:", JSON.stringify(parsedResponse, null, 2));
        } catch (error) {
            console.error("‚ùå Error al analizar JSON de OpenAI:", data.choices[0].message.content);
            console.error("Error details:", error.message);
            return res.status(500).json({ error: "Error al procesar la respuesta de IA" });
        }

        // Validate response structure
        if (!parsedResponse.component || !parsedResponse.status || !Array.isArray(parsedResponse.issues)) {
            console.error("‚ùå Respuesta mal formada de OpenAI:", parsedResponse);
            return res.status(500).json({ error: "Respuesta estructurada inv√°lida de OpenAI" });
        }

        // Validate status is in predefined list
        if (!predefinedConditions.statuses.includes(parsedResponse.status)) {
            console.error("‚ùå Estado inv√°lido recibido:", parsedResponse.status);
            return res.status(500).json({ error: "Estado fuera de los valores predefinidos" });
        }

        // Filter issues to only include predefined ones
        parsedResponse.issues = parsedResponse.issues.filter(issue => predefinedConditions.issues.includes(issue));

        // Send successful response
        console.log("‚úÖ Respuesta procesada correctamente:", JSON.stringify(parsedResponse, null, 2));
        return res.status(200).json({ result: parsedResponse });

    } catch (error) {
        console.error('‚ùå Error en el procesamiento:', error);
        return res.status(500).json({ error: 'Error al procesar la solicitud' });
    }
}
